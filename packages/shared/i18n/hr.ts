// Deep type that widens string literals to string
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
    ? string[]
    : T[K] extends object
    ? DeepStringify<T[K]>
    : T[K];
};

